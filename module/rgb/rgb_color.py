def getRGBByName(name):
    global RGB_TABLE
    if ("色" in name):
        try:
            result = RGB_TABLE[name]
        except KeyError:
            try:
                result = RGB_TABLE[name.replace("色", "")]
            except KeyError:
                return {}
    else:
        try:
            result = RGB_TABLE[name]
        except KeyError:
            try:
                result = RGB_TABLE[name + "色"]
            except KeyError:
                return {}
    return result


